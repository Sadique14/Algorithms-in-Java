package grahamScan;

public class Coordinate {

	double x;
	double y;
	double angle;
	public Coordinate()
	{
		x=0;
		y=0;
		angle = 0.0;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
